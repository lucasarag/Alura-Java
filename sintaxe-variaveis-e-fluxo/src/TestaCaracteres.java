
public class TestaCaracteres {

	public static void main(String[] args) {
		
		// char guarda somente 1 caractere somente em aspas simples ''
		char letra = 'A';
		System.out.println(letra);
		
		char valor = 66;
		System.out.println(valor);
		
		valor = (char) (valor + 1);
		System.out.println(valor);
		
		String palavra = "Lucas aragão";
		System.out.println(palavra);
		
		palavra = palavra + 1997;
		System.out.println(palavra);
	}
	
}
